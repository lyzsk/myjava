package cn.sichu.myjava.august2021.binarytreeproblem;

public class TreeNode {
    int val;
    TreeNode left;
    TreeNode right;

    TreeNode() {

    }

    public TreeNode(int val) {
        super();
        this.val = val;
    }

    public TreeNode(int val, TreeNode left, TreeNode right) {
        super();
        this.val = val;
        this.left = left;
        this.right = right;
    }

}
